class World(object):
    def __init__(self, carla_world, reporter, args):
        self.world = carla_world
        self.sync = args.sync
        self.actor_role_name = args.rolename
        try:
            self.map = self.world.get_map()
        except RuntimeError as error:
            print('RuntimeError: {}'.format(error))
            print('  The server could not send the OpenDRIVE (.xodr) file:')
            print('  Make sure it exists, has the same name of your town, and is correct.')
            sys.exit(1)
        self.reporter = reporter
        self.player = None
        self.collision_sensor = None
        self.lane_invasion_sensor = None
        self.gnss_sensor = None
        self.imu_sensor = None
        self.radar_sensor = None
        self.camera_manager = None
        self._weather_presets = find_weather_presets()
        self._weather_index = 0
        self._actor_filter = args.filter
        self._actor_generation = args.generation
        self._gamma = args.gamma
        self.restart()
        self.world.on_tick(reporter.on_world_tick)
        self.recording_enabled = False
        self.recording_start = 0
        self.constant_velocity_enabled = False
        self.show_vehicle_telemetry = False
        self.doors_are_open = False
        self.current_map_layer = 0
        self.map_layer_names = [
            carla.MapLayer.NONE,
            carla.MapLayer.Buildings,
            carla.MapLayer.Decals,
            carla.MapLayer.Foliage,
            carla.MapLayer.Ground,
            carla.MapLayer.ParkedVehicles,
            carla.MapLayer.Particles,
            carla.MapLayer.Props,
            carla.MapLayer.StreetLights,
            carla.MapLayer.Walls,
            carla.MapLayer.All
        ]

    def restart(self):
        self.player_max_speed = 1.589
        self.player_max_speed_fast = 3.713
        # Keep same camera config if the camera manager exists.
        cam_index = self.camera_manager.index if self.camera_manager is not None else 0
        cam_pos_index = self.camera_manager.transform_index if self.camera_manager is not None else 0
        # Get a random blueprint.
        blueprint_list = get_actor_blueprints(self.world, self._actor_filter, self._actor_generation)
        if not blueprint_list:
            raise ValueError("Couldn't find any blueprints with the specified filters")
        blueprint = random.choice(blueprint_list)
        blueprint.set_attribute('role_name', self.actor_role_name)
        if blueprint.has_attribute('terramechanics'):
            blueprint.set_attribute('terramechanics', 'true')
        if blueprint.has_attribute('color'):
            color = random.choice(blueprint.get_attribute('color').recommended_values)
            blueprint.set_attribute('color', color)
        if blueprint.has_attribute('driver_id'):
            driver_id = random.choice(blueprint.get_attribute('driver_id').recommended_values)
            blueprint.set_attribute('driver_id', driver_id)
        if blueprint.has_attribute('is_invincible'):
            blueprint.set_attribute('is_invincible', 'true')
        # set the max speed
        if blueprint.has_attribute('speed'):
            self.player_max_speed = float(blueprint.get_attribute('speed').recommended_values[1])
            self.player_max_speed_fast = float(blueprint.get_attribute('speed').recommended_values[2])

        # Spawn the player.
        if self.player is not None:
            spawn_point = self.player.get_transform()
            spawn_point.location.z += 2.0
            spawn_point.rotation.roll = 0.0
            spawn_point.rotation.pitch = 0.0
            self.destroy()
            self.player = self.world.try_spawn_actor(blueprint, spawn_point)
            self.show_vehicle_telemetry = False
            self.modify_vehicle_physics(self.player)
        while self.player is None:
            if not self.map.get_spawn_points():
                print('There are no spawn points available in your map/town.')
                print('Please add some Vehicle Spawn Point to your UE4 scene.')
                sys.exit(1)
            spawn_points = self.map.get_spawn_points()
            spawn_point = random.choice(spawn_points) if spawn_points else carla.Transform()
            self.player = self.world.try_spawn_actor(blueprint, spawn_point)
            self.show_vehicle_telemetry = False
            self.modify_vehicle_physics(self.player)
        # Set up the sensors.
        self.collision_sensor = CollisionSensor(self.player, self.reporter)
        self.lane_invasion_sensor = LaneInvasionSensor(self.player, self.reporter)
        self.gnss_sensor = GnssSensor(self.player)
        self.imu_sensor = IMUSensor(self.player)
        self.camera_manager = CameraManager(self.player, self.reporter, self._gamma)
        
        self.camera_manager.set_sensor(cam_index, notify=False)
        actor_type = get_actor_display_name(self.player)
        self.reporter.notification(actor_type)

        if self.sync:
            self.world.tick()
        else:
            self.world.wait_for_tick()

    def next_weather(self, reverse=False):
        self._weather_index += -1 if reverse else 1
        self._weather_index %= len(self._weather_presets)
        preset = self._weather_presets[self._weather_index]
        self.reporter.notification('Weather: %s' % preset[1])
        self.player.get_world().set_weather(preset[0])

    def next_map_layer(self, reverse=False):
        self.current_map_layer += -1 if reverse else 1
        self.current_map_layer %= len(self.map_layer_names)
        selected = self.map_layer_names[self.current_map_layer]
        self.reporter.notification('LayerMap selected: %s' % selected)

    def load_map_layer(self, unload=False):
        selected = self.map_layer_names[self.current_map_layer]
        if unload:
            self.reporter.notification('Unloading map layer: %s' % selected)
            self.world.unload_map_layer(selected)
        else:
            self.reporter.notification('Loading map layer: %s' % selected)
            self.world.load_map_layer(selected)

    def toggle_radar(self):
        if self.radar_sensor is None:
            self.radar_sensor = RadarSensor(self.player)
        elif self.radar_sensor.sensor is not None:
            self.radar_sensor.sensor.destroy()
            self.radar_sensor = None

    def modify_vehicle_physics(self, actor):
        #If actor is not a vehicle, we cannot use the physics control
        try:
            physics_control = actor.get_physics_control()
            physics_control.use_sweep_wheel_collision = True
            actor.apply_physics_control(physics_control)
        except Exception:
            pass

    def tick(self, clock):
        self.reporter.tick(self, clock)

    def render(self, display):
        self.camera_manager.render(display)
        #self.reporter.render(display)

    def destroy_sensors(self):
        self.camera_manager.sensor.destroy()
        self.camera_manager.sensor = None
        self.camera_manager.index = None

    def destroy(self):
        if self.radar_sensor is not None:
            self.toggle_radar()
        sensors = [
            self.camera_manager.sensor,
            self.collision_sensor.sensor,
            self.lane_invasion_sensor.sensor,
            self.gnss_sensor.sensor,
            self.imu_sensor.sensor]
        for sensor in sensors:
            if sensor is not None:
                sensor.stop()
                sensor.destroy()
        if self.player is not None:
            self.player.destroy()

from dataclasses import dataclass
@dataclass
class SimulationLiveData():
    server_fps: float = 0.0
    simulation_fps: float = 0.0
    map: str = ""
    elapsed_time: str = ""
    compass: float = 0.0
    heading: str = ""
    speed: float = 0.0
    acceleration: (float, float, float) = (0.0, 0.0, 0.0)
    gyro: (float, float, float) = (0.0, 0.0, 0.0)
    location: (float, float) = (0.0, 0.0)
    gnss: (float, float) = (0.0, 0.0)
    altitude: float = 0.0
    throttle: float = 0.0
    steer: float = 0.0
    brake: float = 0.0
    reverse: bool = False
    hand_brake: bool = False
    manual: bool = False
    gear: int = 0
    road_gradient: float = 0.0


class Reporter():
    '''
    Replaces HUD and sends simulation data over socketio
    '''
    def __init__(self, width, height, socketio: SocketIO):
        self.dim = (width, height) # pixel dimensions of render window
        self.socketio = socketio
        self.server_fps = 0
        self.simulation_time = 0
        self.frame = 0
        self._server_clock = pygame.time.Clock()
        self.simulation_live_data = SimulationLiveData()
        self.road_gradient_estimator = RoadGradientEstimator(window_size=5, ignore_first_n=30)

    def on_world_tick(self, timestamp):
        self._server_clock.tick()
        self.server_fps = self._server_clock.get_fps()
        self.frame = timestamp.frame
        self.simulation_time = timestamp.elapsed_seconds
    
    def tick(self, world, clock):
        '''
        When World ticks, this Reporter also ticks
        '''
        t = world.player.get_transform()
        v = world.player.get_velocity()
        c = world.player.get_control()
        compass = world.imu_sensor.compass
        heading = 'N' if compass > 270.5 or compass < 89.5 else ''
        heading += 'S' if 90.5 < compass < 269.5 else ''
        heading += 'E' if 0.5 < compass < 179.5 else ''
        heading += 'W' if 180.5 < compass < 359.5 else ''

        road_gradient = self.road_gradient_estimator.update(
                t.location.z, # elevation in meters
                math.sqrt(v.x**2 + v.y**2), # keep m/s 
            )
        road_gradient = road_gradient if road_gradient is not None else 0.0

        sim_live_data = SimulationLiveData(
            server_fps = self.server_fps,
            simulation_fps = clock.get_fps(),
            map = world.map.name.split('/')[-1],
            elapsed_time = str(datetime.timedelta(seconds=int(self.simulation_time))),
            compass = compass,
            heading = heading,
            speed = 3.6 * math.sqrt(v.x**2 + v.y**2 + v.z**2),
            acceleration = world.imu_sensor.accelerometer,
            gyro = world.imu_sensor.gyroscope,
            location = (t.location.x, t.location.y),
            gnss = (world.gnss_sensor.lat, world.gnss_sensor.lon),
            altitude = t.location.z,
            throttle = c.throttle,
            steer = c.steer,
            brake = c.brake,
            reverse = c.reverse,
            hand_brake = c.hand_brake,
            manual = c.manual_gear_shift,
            gear = c.gear,
            road_gradient=road_gradient
        )

        self.simulation_live_data = sim_live_data

        # JSONify the dataclass
        sim_live_data = sim_live_data.__dict__

        self.socketio.emit('simulation_live_data', sim_live_data)
    
    def notification(self, text, seconds=2.0):
        self.socketio.emit('reporter_notification', text)

    def error(self, text):
        self.socketio.emit('reporter_error', text)

    
